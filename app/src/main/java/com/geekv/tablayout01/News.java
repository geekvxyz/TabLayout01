public class News {
    String title;
    String content;
    int view;

    public int getView() {
        return view;
    }

    public void setView(int view) {
        this.view = view;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
